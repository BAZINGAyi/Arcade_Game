function max(number1 , number2) {
    if (parseInt(number1) > parseInt(number2)) {
        return number1;
    }else {
        return number2;
    }
}

function min(number1 , number2) {
    if (parseInt(number1) < parseInt(number2)) {
        return number1;
    }else {
        return number2;
    }
}